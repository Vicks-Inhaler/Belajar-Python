# TypeCasing = The process of converting a variable from one data type to another
#              str(), int(), float(), bool()

#=======================================================
Name = "Kadal"
Jeneng = ""
Age = "25"
Ages = 25
GPA = 3.3
Student = True

Age = str(Age)
Ages = int(Ages)
GPA = float(GPA)
Student = bool(Student)
Pelajar = bool(Jeneng)

Age += "1"
Ages += 1

print(Name)
print(Age)
print(Ages)
print(GPA)
print(Student)
print(Pelajar)